//This is the library
#include<iostream>
using namespace std;
//This is the main function
int main() {
    int  a,b,sum,diff,product,quotient,remainder;  //Declaring multiple varibles
    a=15;  //Defining the varible
    b=4;   //Defining the varible
    sum=a+b;
    diff=a-b;
    product=a*b;
    quotient=a/b;
    remainder=a%b;  //using the arithmetic operations
   cout<< "The sum of the two numbers is " <<sum <<endl;
   cout<< "The difference between the two numbers is " <<diff <<endl;
   cout<< "The product of the two numbers is " <<product <<endl ;
   cout<< "The quotient is " <<quotient <<endl ;
   cout<< "The remainder is " <<remainder <<endl ; //printing out the result
} 
   
